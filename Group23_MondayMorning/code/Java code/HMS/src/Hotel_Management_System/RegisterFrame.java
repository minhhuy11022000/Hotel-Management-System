    private void btnCreateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateActionPerformed
        // TODO add your handling code here:
        //get DATA from the field
        String fname = txtFirstName.getText();
        String lname = txtLName.getText();
        String gender = boxGender.getSelectedItem().toString();
        String phone = txtPhone.getText();
        String email = txtEmail.getText();
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        
        if(fname.trim().equals("") || lname.trim().equals("") || phone.trim().equals("")){
                JOptionPane.showMessageDialog(rootPane, "Field must be filled (First name, Last name, Phone, Email).", "Empty field.", JOptionPane.WARNING_MESSAGE);
            }
            else{
                if(customer.registerAcc(fname, lname, gender, phone, email, username, password)){
                    JOptionPane.showMessageDialog(rootPane, "New account added.", "Account added.", JOptionPane.INFORMATION_MESSAGE);
                    LoginFrame logFrame = new LoginFrame();
                    logFrame.setVisible(true);
                    this.dispose();
                }else{
                    JOptionPane.showMessageDialog(rootPane, "New account not added.", "Account added error.", JOptionPane.INFORMATION_MESSAGE);
                }
            }                
    }//GEN-LAST:event_btnCreateActionPerformed

